package ficha2g;

public class Main {

    public static void main(String[] args) {
        System.out.println("1 x 5 = " + 5);
        System.out.println("2 x 5 = " + (5 * 2));
        System.out.println("3 x 5 = " + (5 * 3));
        System.out.println("4 x 5 = " + (5 * 4));
        System.out.println("5 x 5 = " + (5 * 5));
        System.out.println("6 x 5 = " + (5 * 6));
        System.out.println("7 x 5 = " + (5 * 7));
        System.out.println("8 x 5 = " + (5 * 8));
        System.out.println("9 x 5 = " + (5 * 9));
        System.out.println("10 x 5 = " + (5 * 10));

        System.out.println("Segunda Opção");

        int valor = 5;
        System.out.println("1 x 5 = " + valor * 1);
        System.out.println("2 x 5 = " + (valor * 2));
        System.out.println("3 x 5 = " + (valor * 3));
        System.out.println("4 x 5 = " + (valor * 4));
        System.out.println("5 x 5 = " + (valor * 5));
        System.out.println("6 x 5 = " + (valor * 6));
        System.out.println("7 x 5 = " + (valor * 7));
        System.out.println("8 x 5 = " + (valor * 8));
        System.out.println("9 x 5 = " + (valor * 9));
        System.out.println("10 x 5 = " + (valor * 10));

        System.out.println("Opção Má");

        System.out.println("1 x 5= " + (5 * 1) + "\n"
                + "2 x 5= " + (5 * 2) + "\n" + "3 x 5= "
                + (5 * 3) + "\n" + "4 x 5= " + (5 * 4)
                + "\n" + "5 x 5= " + (5 * 5) + "\n"
                + "6 x 5= " + (6 * 1) + "\n" + "7 x 5= "
                + "\n" + (5 * 7) + "8 x 5= " + (5 * 8)
                + "\n" + "9 x 5= " + (5 * 9) + "\n"
                + "10 x 5= " + (5 * 10));
    }
}
